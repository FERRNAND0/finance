export function useT(language: string) {
  const lang =
    translations[language as keyof typeof translations] || translations.en;
  return (key: TKey): string => {
    return (
      (lang as Record<string, string>)[key] ||
      (translations.en as Record<string, string>)[key] ||
      key
    );
  };
}
